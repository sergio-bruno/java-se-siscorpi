    /**
    * Ao perder o foco o sistema verifica a consistência do CPF ou CNPJ
    * 
    */    
    private void tfCnpj_cpfFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfCnpj_cpfFocusLost
        // TODO add your handling code here:
        boolean validou = false, wECpf = false;
        String wVal;
        wVal = tfCnpj_cpf.getText().trim();
        if ( wVal.length() == 11 )
        {
            wECpf = true;
            ClasseValidaCpf cValidaCpf = new ClasseValidaCpf();
            if ( ! cValidaCpf.ValidaCpf( tfCnpj_cpf.getText() ) )
            {    
                JOptionPane.showMessageDialog( null, "CPF informado inválido ",
                "SisCorpI - Informação",
                JOptionPane.INFORMATION_MESSAGE );
                //tfCnpj_cpf.requestFocus();
            }
            else validou = true; 
            cValidaCpf = null;
        }
        else
        {
            ClasseValidaCnpj cValidaCnpj = new ClasseValidaCnpj();
            if ( ! cValidaCnpj.ValidaCnpj( tfCnpj_cpf.getText() ) )
            {    
                JOptionPane.showMessageDialog( null, "CNPJ informado inválido ",
                "SisCorpI - Informação",
                JOptionPane.INFORMATION_MESSAGE );
                //tfCnpj_cpf.requestFocus();
            }
            else validou = true; 
            cValidaCnpj = null;
        }    
        if ( validou )
        {
            // VERIFICAR SE ESTE CPF OU CNPJ PERTENCE A OUTRO CLIENTE
        }
        tfCnpj_cpf.setText( cMascaraCnpjCPF.MascaraCnpjCPF( tfCnpj_cpf.getText() ) );
    }//GEN-LAST:event_tfCnpj_cpfFocusLost
