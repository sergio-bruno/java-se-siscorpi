    private boolean criaCliente() {
        boolean resposta    = false;
        String codigo       = tfCodigo.getText();
        String razao_social = tfRazao_social.getText();
        String fantasia     = tfFantasia.getText();
        String cnpj_cpf     = cRemoveCaracteres.RemoveCaracteres(new String(
                              "./-"),tfCnpj_cpf.getText());
        String ie           = tfIe.getText();
        String endereco     = tfEndereco.getText();
        String bairro       = tfBairro.getText();
        String cidade       = tfCidade.getText();
        String estado       = tfEstado.getText();
        String cep          = tfCep.getText();
        String numero       = tfNumero.getText();
        String fone         = tfFone.getText();
        String fax          = tfFax.getText();
        String celular      = tfCelular.getText();
        String email        = tfEmail.getText();
        Integer tipo        = cbTipo.getSelectedIndex();
        if (codigo.equals("")) {
            JOptionPane.showMessageDialog(null,"Digite o código do cliente","SisCorpI - Atenção",
            JOptionPane.WARNING_MESSAGE);
        } else if (cnpj_cpf.equals("")) {
            JOptionPane.showMessageDialog(null,"Digite o CNPJ/CPF do cliente","SisCorpI - Atenção",
            JOptionPane.WARNING_MESSAGE);
        } else if (razao_social.equals("")) {
            JOptionPane.showMessageDialog(null,"Digite a razão social do cliente","SisCorpI - Atenção",
            JOptionPane.WARNING_MESSAGE);
        } else if (clienteSelecionado == null) {
            clienteSelecionado = new Cliente_fornecedor(codigo,razao_social,
                                 fantasia,cnpj_cpf,ie,endereco,bairro,cidade, 
                                 estado,cep,numero,fone,fax,celular,email,tipo);
            resposta = true;
        } else {
            clienteSelecionado.setRazao_social(razao_social);
            clienteSelecionado.setFantasia(fantasia);
            clienteSelecionado.setCnpj_cpf(cnpj_cpf);
            clienteSelecionado.setIe(ie);
            clienteSelecionado.setEndereco(endereco);
            clienteSelecionado.setBairro(bairro);
            clienteSelecionado.setCidade(cidade); 
            clienteSelecionado.setEstado(estado);
            clienteSelecionado.setCep(cep);
            clienteSelecionado.setNumero(numero);
            clienteSelecionado.setFone(fone);
            clienteSelecionado.setFax(fax);
            clienteSelecionado.setCelular(celular);
            clienteSelecionado.setEmail(email);
            clienteSelecionado.setTipo(tipo);
            resposta = true;
        }
        return resposta;
    }
